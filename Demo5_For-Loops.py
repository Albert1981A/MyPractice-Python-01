for i in range(5):
    print(i)
# will print 0 to 4 => 0, 1, 2, 3, 4

print('---------------------------')

for i in range(2, 5):
    print(i)
# will print 2 to 4 => 2, 3, 4

print('---------------------------')

for i in range(2, 10, 3):
    print(i)
# will print from 2 to 9 in jumps of 3 => will print 2, 5, 8

print('---------------------------')